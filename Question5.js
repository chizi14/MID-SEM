let lights = ["Yellow","Red","Green"]; 

// Using if statement
if (lightsights === "Yellow") {
  console.log("Get ready");
} else if (lights === "Red") {
  console.log("Stop!");
} else if (lights === "Green") {
  console.log("Go!");
}

// Using switch statement
switch (lights) {
  case "Yellow":
    console.log("Get ready!");
    break;
  case "Green":
    console.log("Go!");
    break;
  case "Red":
    console.log("Stop!!!");
    break;
}
